#include "libft.h"

t_list	*ft_lstmap(t_list *lst, void *(*f)(void *), void (*del)(void *))
{
	t_list	*temp;
	t_list	*head;
	void	*tmp;

	head = NULL;
	while (lst)
	{
		tmp = (*f)(lst->content);
		temp = ft_lstnew(tmp);
		if (!temp)
		{
			if (tmp)
				del(tmp);
			ft_lstclear(&head, del);
			return (NULL);
		}
		ft_lstadd_back(&head, temp);
		lst = lst->next;
	}
	return (head);
}
